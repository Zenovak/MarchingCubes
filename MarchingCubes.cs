    private byte cubeIndex(int x, int y, int z) {
        byte cubeConfiguration = 0;

        if (mapArray3D[x, y, z + 1] < Isolevel) 
            cubeConfiguration |= 1;
        if (mapArray3D[x + 1, y, z + 1] < Isolevel) 
            cubeConfiguration |= 2;
        if (mapArray3D[x + 1, y, z] < Isolevel) 
            cubeConfiguration |= 4;
        if (mapArray3D[x, y, z] < Isolevel) 
            cubeConfiguration |= 8;
        if (mapArray3D[x, y + 1, z + 1] < Isolevel) 
            cubeConfiguration |= 16;
        if (mapArray3D[x + 1, y + 1, z + 1] < Isolevel) 
            cubeConfiguration |= 32;
        if (mapArray3D[x + 1, y + 1, z] < Isolevel) 
            cubeConfiguration |= 64;
        if (mapArray3D[x, y + 1, z] < Isolevel) 
            cubeConfiguration |= 128;

        return cubeConfiguration;
    }

    private void MakeTriangles(byte cubeIndex, Vector3 cubeCoords) {
        var edges = GetEdges(cubeIndex);

        for (int i = 0; edges[i] != -1; i += 3) {
            // First edge lies between vertex e00 and vertex e01
            int e00 = edgeConnections[edges[i]][0];
            int e01 = edgeConnections[edges[i]][1];
            Vector3 v00 = cornerOffsets[e00] + cubeCoords;
            Vector3 v01 = cornerOffsets[e01] + cubeCoords;
            Vector3 midpointA = new Vector3((v00.x + v01.x) / 2, (v00.y + v01.y) / 2, (v00.z + v01.z) / 2);

            // Second edge lies between vertex e10 and vertex e11
            int e10 = edgeConnections[edges[i + 1]][0];
            int e11 = edgeConnections[edges[i + 1]][1];
            Vector3 v10 = cornerOffsets[e10] + cubeCoords;
            Vector3 v11 = cornerOffsets[e11] + cubeCoords;
            Vector3 midpointB = new Vector3((v10.x + v11.x) / 2, (v10.y + v11.y) / 2, (v10.z + v11.z) / 2);

            // Third edge lies between vertex e20 and vertex e21
            int e20 = edgeConnections[edges[i + 2]][0];
            int e21 = edgeConnections[edges[i + 2]][1];
            Vector3 v20 = cornerOffsets[e20] + cubeCoords;
            Vector3 v21 = cornerOffsets[e21] + cubeCoords;
            Vector3 midpointC = new Vector3((v20.x + v21.x) / 2, (v20.y + v21.y) / 2, (v20.z + v21.z) / 2);

            // Construct the triangle using the midpoints of the edges
            Triangle tri = new Triangle();
            tri.a = midpointA;
            tri.b = midpointB;
            tri.c = midpointC;
            triangles.Append(tri);
            Debug.Log("Counting Midpoints");
            Debug.Log(midpointA.x);
            var midpoints = new Vector3[3] { midpointA, midpointB, midpointC };

            foreach (var midpoint in midpoints) {
                if (!vertices.Contains(midpoint)) {
                    vertices.Add(midpoint);
                    UniqueIndexes.Add(midpoint, vertices.Count - 1);
                }
                trianglesVertexIndexes.Add(UniqueIndexes[midpoint]);
            }
        }
    }

