    public void March() {
        var width = mapArray3D.GetLength(0);
        var height = mapArray3D.GetLength(1);
        var depth = mapArray3D.GetLength(2);
        cubes = new Cube[width, height, depth];

        vertices = new HashSet<Vector3>();
        triangles = new List<Triangle>();

        Debug.Log("Begin marching");
        Debug.Log(width);
        Debug.Log(height);
        Debug.Log(depth);

        for (int x = 0; x < width - 1; x++) {
            for (int y = 0; y < height - 1; y++) {
                for (int z = 0; z < depth - 1; z++) {
                    var cubeConfigu = cubeIndex(x, y, z);
                    cubes[x, y, z] = new Cube(cubeConfigu);
                    MakeTriangles(cubeConfigu, new Vector3(x, y, z));
                    Debug.Log("Making triangles");
                }
            }
        }
        GenerateMeshReadyComponents();
    }

